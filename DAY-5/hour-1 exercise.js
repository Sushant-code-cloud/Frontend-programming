let day=3;
let dayname;
switch (day){
    case 0 : dayname="sunday";
    break;
    case 1 : dayname="monday";
    break;
    case 2 : dayname="tuesday";
    break;
    case 3 : dayname="wednesday";
    break;
    case 4 : dayname="thrusday";
    break;
    case 5 : dayname="friday";
    break;
    case 6 : dayname="saturday";
    break;
    
    default :dayname="invalid day range";
    
}
console.log(dayname);
